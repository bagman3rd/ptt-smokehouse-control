import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
const nav = readFileSync('components/NavMenu.tsx', 'utf8');
const shell = readFileSync('components/Nav.tsx', 'utf8');
assert.ok(nav.includes("'use client'"), 'navigation must use the tested interactive dropdown component');
assert.ok(nav.includes('onClick={() => setOpenGroup'), 'dropdown buttons must explicitly toggle state');
assert.ok(nav.includes("event.key === 'Escape'"), 'dropdowns must close on Escape');
assert.ok(nav.includes("document.addEventListener('pointerdown'"), 'dropdowns must close on outside click/tap');
assert.ok(nav.includes('aria-expanded={isOpen}'), 'dropdown buttons must expose state');
assert.ok(nav.includes('role="menu"') && nav.includes('role="menuitem"'), 'dropdown semantics must be present');
for (const label of ['Operations', 'Insights', 'Admin', 'Help']) assert.ok(shell.includes(`label: '${label}'`));
assert.ok(shell.includes('Build 8.0.3'));
console.log('Build 8.0.3 interactive dropdown navigation checks passed.');
