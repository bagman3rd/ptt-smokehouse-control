import fs from 'node:fs';
import assert from 'node:assert/strict';
const read = (f) => fs.readFileSync(f, 'utf8');
const pkg = JSON.parse(read('package.json'));
assert.equal(pkg.version, '6.1.0');
assert.equal(pkg.packageManager, 'pnpm@9.15.0');
assert(!pkg.scripts['render-build'].includes('seed.ts'), 'Render build must not auto-seed production');
assert(pkg.scripts['render-build'].includes('reconcile-migration-history'));
const workflow = read('.github/workflows/ci.yml');
assert(workflow.includes('Build 6.1.0 CI'));
assert(workflow.includes('pnpm run test:e2e'));
assert(workflow.includes('playwright install'));
const codes = read('lib/domainCodes.ts');
for (const value of ['OVERNIGHT_ONLY','SAME_DAY_ONLY','FLEXIBLE','BACKUP_OVERFLOW','INACTIVE','INDOOR_HOOD','BRISKET','CHICKEN']) assert(codes.includes(value));
const migrations = fs.readdirSync('prisma/migrations').filter((x) => /^\d+_/.test(x)).sort();
const prefixes = migrations.map((x) => x.split('_')[0]);
assert.equal(new Set(prefixes).size, prefixes.length, 'migration timestamp prefixes must be unique');
assert(read('prisma/migrations/20260712000700_build_610_reliability_domain_codes/migration.sql').includes('Protein_code_check'));
assert(read('e2e/core-workflow.spec.ts').includes("test('login and core kitchen pages render'"));
console.log('Build 6.1.0 evaluation passed.');
